let lang = 'en';
function applyLang(l) {
  const t = T[l];
  Object.keys(t).forEach(id => {
    if (id === 'fi-name-ph') { const el = document.getElementById('fi-name'); if (el) el.placeholder = t[id]; return; }
    if (id === 'fi-project-ph') { const el = document.getElementById('fi-project'); if (el) el.placeholder = t[id]; return; }
    const el = document.getElementById(id);
    if (el) el.innerHTML = t[id];
  });
  document.querySelectorAll('.pj-title').forEach(el => {
    const item = el.closest('.portfolio-item');
    if (item) { const title = l === 'en' ? item.dataset.titleEn : item.dataset.titleEs; if (title) el.textContent = title; }
  });
  document.documentElement.lang = l;
}
function toggleLang() {
  lang = lang === 'en' ? 'es' : 'en';
  applyLang(lang);
  document.getElementById('lang-flag').textContent = lang === 'en' ? '\u{1F1EA}\u{1F1F8}' : '\u{1F1EC}\u{1F1E7}';
  document.getElementById('lang-label').textContent = lang === 'en' ? 'ES' : 'EN';
}
applyLang('en');
const obs = new IntersectionObserver((entries) => {
  entries.forEach((e, i) => { if (e.isIntersecting) { setTimeout(() => e.target.classList.add('visible'), i * 80); obs.unobserve(e.target); } });
}, { threshold: 0.1 });
document.querySelectorAll('.reveal').forEach(el => obs.observe(el));
function filterProjects(cat, btn) {
  document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
  btn.classList.add('active');
  const items = document.querySelectorAll('.portfolio-item');
  items.forEach(item => {
    item.classList.remove('featured');
    if (cat === 'selection') {
      item.style.display = item.dataset.featured === 'true' ? '' : 'none';
    } else {
      item.style.display = item.dataset.cat === cat ? '' : 'none';
    }
  });
  if (cat === 'selection') {
    document.querySelectorAll('[data-featured="true"]').forEach(item => item.classList.add('featured'));
  } else {
    let first = true;
    items.forEach(item => {
      if (item.style.display !== 'none' && first) { item.classList.add('featured'); first = false; }
    });
  }
}
